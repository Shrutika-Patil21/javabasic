package Program;

public class AccessingMethods {

	public static void main(String[] args) 
	{
		System.out.println("Shrutika Ganesh Patil");
	add();
	mul();
	sub();
	}

	static void add()
	{
		System.out.println("Adding");
	}
	
	static void mul()
	{
		System.out.println("Multiplication");
	}
	
	static void sub()
	{
		System.out.println("Subtraction");
	}
	
	
	
	
	
	
}
