package Program;

public class GlobalVariableStatic {
static int a=50;
static int d=46;
static void add()
{
	System.out.println(a+d);
}
static void sub()
{
	System.out.println(a-d);
}
static void mul()
{
	System.out.println(a*d);
}
	public static void main(String[] args) {
		add();
		sub();
		mul();

	}

}
