package Program;

public class Paramitarizedmethod {
	static void add(int a, int b)
	{
		System.out.println(a+b);
	}
	static void sub(int a, int b)
	{
		System.out.println(a-b);
	}
	static void mul()
	{
		int a=40000;
		int b=56;
		System.out.println(a*b);
	}
	

	public static void main(String[] args) {
		mul();
		add(1987,3243);
		sub(3243,43543);	
		

	}

}
