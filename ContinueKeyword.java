package Program;

public class ContinueKeyword {

	public static void main(String[] args) {
		for(int i=0;i<=5;i++)
		{
			
			if(i==4)
			{
				
			System.out.println("Let's Skip " +i);
			continue;
			}
			if(i==2)
			{
				
			System.out.println("Let's Skip " +i);
			continue;
			}
			
	System.out.println("Manish " +i);
		}
	}

}
