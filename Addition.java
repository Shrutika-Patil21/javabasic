package Program;

public class Addition {
	static void add()
	{
	int a=100;
	int b=200;
	int c=a+b;
	System.out.println(c);
	
	}
	
	static void sub()
	{
	int a=100;
	int b=200;
	int c=a-b;
	System.out.println(c);
	
	}
	static void mul()
	{
	int a=100;
	int b=200;
	int c=a*b;
	System.out.println(c);
	
	}
	
	static void div()
	{
	int a=20000;
	int b=2;
	int c=a/b;
	System.out.println(c);
	
	}
	
	static void mod()
	{
	int a=109;
	int b=5;
	int c=a%b;
	System.out.println(c);
	
	}

	public static void main(String[] args) 
{
		add();
		sub();
		mul();
		div();
		mod();

	}

}
